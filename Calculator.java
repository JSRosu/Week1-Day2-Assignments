public class Calculator {

	public int add() {
		int num1 = 12;
		int num2 = 21;
		int num3 = 1;
		int addition = num1+num2+num3;
		return addition;
	}
	
	public int sub() {
		int num1 = 54;
		int num2 = 10;
		int subtraction=num1-num2;
		return subtraction;
}
	public double mul() {
		double num1 = 12345563;
		double num2=124543;
		double multiplication=num1*num2;
		return multiplication;
	}
	public float div() {
		float num1 = 12.10f;
		float num2=1.23f;
		float division = num1/num2;
		return division;
	}
	
}